class SocialGroups:
    def __init__(self, economic_groups, political_groups):
        self.economic_groups = economic_groups  # Экономические группы, разделенные по уровню дохода, профессии, типу занятости и т.д.
        self.political_groups = political_groups  # Политические группы, разделенные по политической принадлежности, активности, уровню участия и т.д.