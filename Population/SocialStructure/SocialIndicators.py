class SocialIndicators:
    def __init__(self, education, employment, healthcare, income_inequality, poverty_rate):
        self.education = education  # информация о системе образования
        self.employment = employment  # статистика о занятости
        self.healthcare = healthcare  # данные о системе здравоохранения
        self.income_inequality = income_inequality  # показатель неравенства доходов, например, коэффициент Джини
        self.poverty_rate = poverty_rate  # уровень бедности в стране